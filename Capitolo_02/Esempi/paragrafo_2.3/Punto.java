public class Punto {
    public int x;
    public int y;
    
    public Punto() { // Costruttore: nessun tipo di ritorno e stesso nome della classe
        System.out.println("Costruito un Punto!");
    }

    public Punto(int a, int b) {
        x = a;
        y = b;
    }
}

