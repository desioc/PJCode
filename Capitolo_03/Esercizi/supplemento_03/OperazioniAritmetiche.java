void main() {
    int a = 5, b = 3;
    double r1 = (double)a/b;
    println("r1 = " + r1);
    char c = 'a';
    short s = 5000;
    int r2 = c*s;
    println("r2 = " + r2);
    int i = 6;
    float f = 3.14F;
    float r3 = i + f;
    println("r3 = " + r3);
    double r4 = r1 - r2 - r3;
    println("r4 = " + r4);
}