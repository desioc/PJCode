public class EsempioUnicode {
    public static void main(String[] args) {
        char carattere = '\u00E8'; // Carattere 'è' in Unicode
        System.out.println("Carattere: " + carattere);

        // Utilizzo di un'emoji
        String emoji = "\uD83D\uDE00"; // Emoji ?
        System.out.println("Emoji: " + emoji);
    }
}
