/// Questa classe si chiama `Commenti`
public class Commenti {
// ESEMPIO DI COMMENTO JAVADOC CON TAG JAVADOC 
    /**
     * Questo metodo somma due numeri interi. 
     *
     * @param a Il primo numero da sommare.
     * @param b Il secondo numero da sommare.
     * @return La somma dei due numeri.
     * Per chiamare il metodo {@code somma}:
     * {@snippet :
     * Commenti commenti = new Commenti();
     * commenti.somma(2,6);
     * }
     */
    public int somma(int a, int b) {
        return a + b;
    } 
}